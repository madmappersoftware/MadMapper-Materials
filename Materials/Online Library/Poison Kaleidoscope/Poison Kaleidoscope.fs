#include "MadCommon.glsl"

float mat_time = mat_time_source - mat_offset * 8. * mat_offset_scale;

vec2 matRot2D(vec2 _st, float _angle){
    _st -= 0.5;
    _st =  mat2(cos(_angle),-sin(_angle),
                sin(_angle),cos(_angle)) * _st;
    _st += 0.5;
    return _st;
}

float sdSquircle(vec2 uv, vec2 origin, float radius, float power, float rot_)
{
    float c = cos(rot_) * mat_k_cycle_1, s = sin(rot_);
    mat2 rot = mat2(c,s,-s,c);
    vec2 v = abs((origin*rot) - (uv*rot));
    v *= mat_k_scale_2;
    float d = pow(v.x,power) + pow(v.y, power);
    d -= pow(radius, power) * mat_k_cycle_2;
    return d;
}
vec3 sdWhatever(vec2 uv, float r0, float r, float p, float t)
{
  float ab = 0.01 * mat_aberration;
  return vec3(
      sdSquircle(uv, uv+r0-mod(uv,r0*2.), r-ab, p+ab, t+ab),
      sdSquircle(uv, uv+r0-mod(uv,r0*2.), r, p, t),
      sdSquircle(uv, uv+r0-mod(uv,r0*2.), r+ab, p+ab, t+ab)
             );
}

float rand(vec2 co)
{
   return fract(sin(dot(co.xy,vec2(12.9898,78.233))) * 43758.5453);
}


vec4 materialColorForPixel(vec2 texCoord)
{
    vec4 out_color; // Final output color
    vec2 uv = texCoord - vec2(0.5);
    uv *= mat_scale;

    uv += vec2(0.5);
    uv = matRot2D(uv, 2*PI*mat_rotate / 360);
    uv -= vec2(0.5);

    vec2 uv_shift = mat_shift_amount;
    uv_shift += vec2(0.5);
    uv_shift.x = 1.-uv_shift.x;
    uv_shift -= vec2(0.5);

    // Allow for more logical XY shifts in the presence of a custom rotation (for user input)
    uv_shift += vec2(0.5);
    uv_shift = matRot2D(uv_shift, 2*PI*(mat_rotate) / 360);
    uv_shift -= vec2(0.5);

    uv += uv_shift;

    vec2 uv_orig = uv;

    float t = mat_time*.15;
    vec3 dist;
    float r0 = 0.4 / mat_k_scale_1;
    float p = 2.;
    float r = r0;
    for(float i = 0.; i < 21.; ++ i)
    {
        vec3 dist1 = sdWhatever(uv, r0, r, p,t) * mat_k_limit;
        dist = max(-dist, -dist1);
        dist1 = sdWhatever(uv-r0, r0, r, p,-t);
        dist = min(dist, dist1);
        r = r*0.85 + 0.01;
        p = sin(mat_time*.22+i)*.5+1.;
        p *= mat_k_lvl_1;
        t = -t-.2 - mat_spin_offset * 2.*PI;

    }
    out_color.rgb = mix(vec3(.9,.9,.6), vec3(0.,0.,0.), 0.7*clamp(1.0 / (clamp(dist, 1.0/250., 1.0)*250.),0.,1.));
    vec2 uvn = uv_orig * mat_vig_scale;

    out_color -= rand(uv + mat_time)*.07 * mat_dither;
    out_color *= 1.-dot(uvn,uvn*2.1) * mat_vig_level;
    out_color.a = 1.;

    // Apply invert
    if (mat_invert) out_color.rgb=1-out_color.rgb;

    // Apply Hue Shift and saturation
    if (mat_hue_shift > 0.01 || mat_saturation != 0) {
        vec3 hsv = rgb2hsv(out_color.rgb);
        hsv.x = fract(0.9999999*(hsv.x+mat_hue_shift));
        hsv.y = max(hsv.y + mat_saturation, 0);
        out_color.rgb = hsv2rgb(hsv);
    }

    // Apply contrast
    const vec3 LumCoeff = vec3(0.2125, 0.7154, 0.0721);
    const vec3 AvgLumin = vec3(0.5, 0.5, 0.5);
    vec3 intensity = vec3(dot(out_color.rgb, LumCoeff));
    out_color.rgb = mix(AvgLumin, out_color.rgb, mat_contrast);

    // Apply brightness
    out_color.rgb += mat_brightness;


    return out_color;
}
