#include "MadCommon.glsl"

float mat_time = (mat_time_source - mat_offset * 16. * mat_offset_scale) * 2.;
float mat_light_time = (mat_light_time_source - mat_light_offset * 8.) * 2.;

vec2 matRot2D(vec2 _st, float _angle){
    _st -= 0.5;
    _st =  mat2(cos(_angle),-sin(_angle),
                sin(_angle),cos(_angle)) * _st;
    _st += 0.5;
    return _st;
}

float mat_luma(vec3 color) {
  return dot(color, vec3(0.299, 0.587, 0.114));
}

float mat_luma(vec4 color) {
  return dot(color.rgb, vec3(0.299, 0.587, 0.114));
}

vec2 mirrorUV(vec2 uv) {
    uv += vec2(0.5);
    if (mat_mirror_x) {
        if (uv.x > 0.5)   {
            uv.x = 1.0-uv.x;
        }
    }
    if (mat_mirror_y) {
        if (uv.y > 0.5) {
            uv.y = 1.0-uv.y;
        }
    }
    uv -= vec2(0.5);
    return uv;
}

vec2 transformUV(vec2 uv) {

    if (mat_lock_aspect) {
        uv.x *= RENDERSIZE.x / RENDERSIZE.y;
    }
    uv *= mat_scale * 1.1;

    uv = mirrorUV(uv);

    vec2 uv_shift = mat_shift_amount * mat_shift_scale;
    uv_shift += vec2(0.5);
    uv_shift.x = 1.-uv_shift.x;
    uv_shift -= vec2(0.5);

    // XY shift pre rotate
    if (mat_shift_type == 0) {

        // Preserve direction
        uv_shift += vec2(0.5);
        uv_shift = matRot2D(uv_shift, -1. * 2*PI*(mat_rotate) / 360);
        uv_shift -= vec2(0.5);
        uv += uv_shift;
    }

    uv += vec2(0.5);
    uv = matRot2D(uv, 2*PI*mat_rotate / 360);
    uv -= vec2(0.5);

    // XY shift post rotate
    if (mat_shift_type == 1) {

        // Preserve direction
        uv_shift += vec2(0.5);
        uv_shift = matRot2D(uv_shift, 2*PI*(mat_rotate) / 360);
        uv_shift -= vec2(0.5);
        uv += uv_shift;
    }

    return uv;
}

float f(vec2 pp)
{
    //if (abs(pp.y) > .5)return 0.;
    vec3 ht = smoothstep(0., 3., mat_warp2*.25 - dot(pp, pp)) * vec3(pp, 10.) * pow(mat_warp1,1.5);
    vec3 n = 100. * normalize(-ht - vec3(.001, .002, 1));

    n.xy *= mat_noise_scale;

    vec2 noise_offset = mat_noise_offset * mat_noise_offset_scale * 0.5;
    noise_offset += vec2(0.5);
    noise_offset.y = 1.-noise_offset.y;
    noise_offset -= vec2(0.5);
    n.xy += noise_offset;

    vec3 p = n;
    float tm = .35 * mat_time;
    for (float i = 0.; i <= float(mat_iterations); i++)
    {
        p = 16. * n + vec3(cos(tm + i - p.x) + cos(tm + i - p.y), sin(i - p.y) + cos(i + p.x), 1);
        p.xy = n.yz + (cos(i) * p.xy + sin(i) * abs(vec2(p.y, -p.x)));
    }
    return (dot(vec3(4, -1, 100), -p)) * pow(mat_glow, 1.5) * 2.;
}


vec4 materialColorForPixel(vec2 texCoord)
{
    vec4 out_color; // Final output color
    vec2 uv = texCoord - vec2(0.5);

    uv = transformUV(uv);

    vec2 p = uv;

    const vec2 e = vec2(.001, .001);
    vec3 sn = normalize(vec3(f(p + e) - f(p - e), f(p + e.yx) - f(p - e.yx), -.5));
    out_color = vec4(.95 - vec3(clamp(dot(-normalize(1. - length(p - vec2(sin(mat_light_time), cos(mat_light_time)))+ vec3(pow(mat_backlight,2.)*2. * p, 6)),sn),.05, .8/mat_haze)),1);

    // Apply invert
    if (mat_invert) out_color.rgb=1-out_color.rgb;

    // Apply contrast
    const vec3 LumCoeff = vec3(0.2125, 0.7154, 0.0721);
    const vec3 AvgLumin = vec3(0.5, 0.5, 0.5);
    vec3 intensity = vec3(dot(out_color.rgb, LumCoeff));
    out_color.rgb = mix(AvgLumin, out_color.rgb, mat_contrast);

    out_color.rgb += mat_brightness;

    // General way to add transparency to any shader
    if (mat_back_mode == 1) {
        // Differentiate front & back colors using a hard cut w/ threshold
        if (mat_luma(out_color.rgb) < mat_back_thresh) {
            out_color = mat_back_color;
        } else {
            out_color = mat_front_color;
        }
    } else {
        // Differentiate front & back colors using the gradual mix based on luma + a threshold used as an offset
        out_color = mix(mat_back_color, mat_front_color, mat_luma(out_color.rgb) * mat_back_sensitivity + mat_back_thresh);
    }



    return out_color;
}
