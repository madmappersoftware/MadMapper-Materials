#include "MadCommon.glsl"

float mat_time = (mat_time_source - mat_offset * 8. * mat_offset_scale) * 0.5;

float mat_shift_time = (mat_shift_time_source - mat_shift_offset) * 0.12;

float mat_luma(vec3 color) {
  return dot(color, vec3(0.299, 0.587, 0.114));
}

float mat_luma(vec4 color) {
  return dot(color.rgb, vec3(0.299, 0.587, 0.114));
}

vec2 matRot2D(vec2 _st, float _angle){
    _st -= 0.5;
    _st =  mat2(cos(_angle),-sin(_angle),
                sin(_angle),cos(_angle)) * _st;
    _st += 0.5;
    return _st;
}

vec2 mirrorUV(vec2 uv) {
    uv += vec2(0.5);
    if (mat_mirror_x) {
        if (uv.x > 0.5)   {
            uv.x = 1.0-uv.x;
        }
    }
    if (mat_mirror_y) {
        if (uv.y > 0.5) {
            uv.y = 1.0-uv.y;
        }
    }
    uv -= vec2(0.5);
    return uv;
}

vec2 transformUV(vec2 uv) {

    if (mat_lock_aspect) {
        uv.x *= RENDERSIZE.x / RENDERSIZE.y;
    }
    uv *= mat_scale;

    uv = mirrorUV(uv);

    vec2 uv_shift = mat_shift_amount * mat_shift_scale;
    uv_shift += vec2(0.5);
    uv_shift.x = 1.-uv_shift.x;
    uv_shift -= vec2(0.5);

    // XY shift pre rotate
    if (mat_shift_type == 0) {

        // Preserve direction
        uv_shift += vec2(0.5);
        uv_shift = matRot2D(uv_shift, -1. * 2*PI*(mat_rotate) / 360);
        uv_shift -= vec2(0.5);
        uv += uv_shift;
    }

    uv += vec2(0.5);
    uv = matRot2D(uv, 2*PI*mat_rotate / 360);
    uv -= vec2(0.5);

    // XY shift post rotate
    if (mat_shift_type == 1) {

        // Preserve direction
        uv_shift += vec2(0.5);
        uv_shift = matRot2D(uv_shift, 2*PI*(mat_rotate) / 360);
        uv_shift -= vec2(0.5);
        uv += uv_shift;
    }

    return uv;
}


#define h21(a) fract(sin(dot(a, vec2(13,79))) * 4e5 )
#define thc(a,b) tanh(a * cos(b)) / tanh(a)

vec3 pal( in float t, in vec3 a, in vec3 b, in vec3 c, in vec3 d ) {
    return a + b*cos( 6.28318*(c*t+d) );
}

vec4 materialColorForPixel(vec2 texCoord)
{
    vec4 out_color; // Final output color
    vec2 uv = texCoord - vec2(0.5);

    uv = transformUV(uv);

    float time = 0.45 * mat_time;

    if (mat_shift_animate) {
        float shift_time_x = mat_shift_time * cos(2.*PI * mat_shift_angle/360.0);
        float shift_time_y = mat_shift_time * sin(2.*PI * mat_shift_angle/360.0);
        uv.x -= shift_time_x;
        uv.y -= shift_time_y;
    }


    float sc = 10.;
    vec2 ipos = floor(sc * uv) + 0.5;
    vec2 fpos = fract(sc * uv) - 0.5;

    float h = h21(ipos);
    float h2 = 2. * PI * h21(ipos + 2.);

    vec2 p = fract(fpos - 4. * thc(2., vec2(h2, 2. * PI * h) + time)) - 0.5;

    float d = length(p) * mat_obj_scale;
    float k = 1. + h;
    float s = smoothstep(-k, k, -d + 0.2);

    vec2 pw = mat_smooth * vec2(5. + 4. * thc(20., 8. * mat_pattern_scale * d + h2 + time));
    vec2 b = pow(1. - 4. * fpos * fpos, pw);

    vec3 e = vec3(1.5);
    // 50% chance of having white in it, .5+.5*cos gives white to some of it
    float cp = step(0.5 + 0.5 * step(h, 0.5) * cos(8. * d), 0.5 * mat_colorize);
    vec3 col = pal(h2 + 0.25 * d, e, e, e, cp * vec3(0.,0.33,0.66) * mat_color_cycle);
    col *= b.x * b.y * s; // * step(h21(ipos + 1.), 0.5);
    col += 0.16;
    out_color = vec4(col,1.0);


    // Luma to alpha (before color controls)
    if (mat_luma_to_alpha && mat_luma_mode == 0) {
        out_color.a = mat_luma(out_color * mat_luma_sensitivity) - mat_luma_threshold * 4. - 1.;
    }

    // Apply invert
    if (mat_invert) out_color.rgb=1-out_color.rgb;

    // Apply Hue Shift and saturation
    if (mat_hue_shift > 0.01 || mat_saturation != 0) {
        vec3 hsv = rgb2hsv(out_color.rgb);
        hsv.x = fract(0.9999999*(hsv.x+mat_hue_shift));
        hsv.y = max(hsv.y + mat_saturation, 0);
        out_color.rgb = hsv2rgb(hsv);
    }

    // Apply contrast
    const vec3 LumCoeff = vec3(0.2125, 0.7154, 0.0721);
    const vec3 AvgLumin = vec3(0.5, 0.5, 0.5);
    vec3 intensity = vec3(dot(out_color.rgb, LumCoeff));
    out_color.rgb = mix(AvgLumin, out_color.rgb, mat_contrast);

    // Apply brightness
    out_color.rgb += mat_brightness;

    // Luma to alpha (after color controls)
    if (mat_luma_to_alpha && mat_luma_mode == 1) {
        out_color.a = mat_luma(out_color * mat_luma_sensitivity);
    }

    return out_color;
}
