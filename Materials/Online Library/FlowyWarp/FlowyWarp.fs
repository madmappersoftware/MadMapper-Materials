    vec2 pos = texCoord + mouse / 4.0;

    pos *= mat_zoom;

    pos.x += cos(pos.y*5.)*.2;
    pos.y += cos(pos.y*5.0+iTime)*.2+sin(pos.x*10.0+iTime)*.2;

