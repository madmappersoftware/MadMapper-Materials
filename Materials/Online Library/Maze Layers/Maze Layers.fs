#include "MadCommon.glsl"

float mat_time = mat_time_source - mat_offset;

// hash without sine
// https://www.shadertoy.com/view/4djSRW
#define MOD3 vec3(443.8975, 397.2973, 491.1871) // uv range
float hash13(vec3 p3) {
    p3  = fract(p3 * MOD3);
    p3 += dot(p3, p3.yzx + 19.19);
    return fract((p3.x + p3.y) * p3.z);
}

float map(vec3 p) {

    vec3 p2 = p+vec3(p.y, -p.x, 0.);

    return
        float(
            hash13(floor(p2))<.5 ?
            2. * abs(fract(p.x)-.5) :
            2. * abs(fract(p.y)-.5)
        );

}

vec4 materialColorForPixel(vec2 texCoord)
{
    vec4 out_color; // Final output color
    vec2 uv = texCoord - vec2(0.5);
    uv *= mat_scale;

    vec2 uv_shift = mat_shift_amount;
    uv_shift += vec2(0.5);
    uv_shift.x = 1.-uv_shift.x;
    uv_shift -= vec2(0.5);
    uv += uv_shift;

    vec2 p = uv;

    float f = 0.;

    vec3 cpos = vec3(0.);
    vec3 cdir = 4. * vec3(p, 1.);

    float time_x = mat_time * cos(PI * mat_angle);
    float time_y = mat_time * sin(PI * mat_angle);

    cpos.x += time_x;
    cpos.y += time_y;

    float alpha = 1.;
    float I = mat_layers;
    for(float i = 1.; i<=I; i++) {
        cpos += cdir;
        float m = map(cpos) + .13;
        alpha -= 1.-step(.5, m);
        if(alpha<=0.)
            break;
        f = mix(f, pow(m, .25)*i/I, alpha);
    }

    out_color = vec4(vec3(f)*vec3(1.5, 1.2, 1.), 1.);




    // Apply invert
    if (mat_invert) out_color.rgb=1-out_color.rgb;

    // Apply Hue Shift and saturation
    if (mat_hue_shift > 0.01 || mat_saturation != 0) {
        vec3 hsv = rgb2hsv(out_color.rgb);
        hsv.x = fract(0.9999999*(hsv.x+mat_hue_shift));
        hsv.y = max(hsv.y + mat_saturation, 0);
        out_color.rgb = hsv2rgb(hsv);
    }

    // Apply contrast
    const vec3 LumCoeff = vec3(0.2125, 0.7154, 0.0721);
    const vec3 AvgLumin = vec3(0.5, 0.5, 0.5);
    vec3 intensity = vec3(dot(out_color.rgb, LumCoeff));
    out_color.rgb = mix(AvgLumin, out_color.rgb, mat_contrast);

    // Apply brightness
    out_color.rgb += mat_brightness;


    return out_color;
}
