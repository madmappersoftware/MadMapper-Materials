#include "MadCommon.glsl"

float mat_time = mat_time_source - mat_offset * 8. * mat_offset_scale;

vec2 matRot2D(vec2 _st, float _angle){
    _st -= 0.5;
    _st =  mat2(cos(_angle),-sin(_angle),
                sin(_angle),cos(_angle)) * _st;
    _st += 0.5;
    return _st;
}

float mat_luma(vec3 color) {
  return dot(color, vec3(0.299, 0.587, 0.114));
}

float mat_luma(vec4 color) {
  return dot(color.rgb, vec3(0.299, 0.587, 0.114));
}


mat2 Rot(float a) {
    float s=sin(a), c=cos(a);
    return mat2(c, -s, s, c);
}


vec4 materialColorForPixel(vec2 texCoord)
{
    vec4 out_color; // Final output color
    vec2 uv = texCoord - vec2(0.5);
    uv *= mat_scale * 1.5;

    uv += vec2(0.5);
    uv = matRot2D(uv, 2*PI*mat_rotate / 360);
    uv -= vec2(0.5);

    vec2 uv_shift = mat_shift_amount;
    uv_shift += vec2(0.5);
    uv_shift.x = 1.-uv_shift.x;
    uv_shift -= vec2(0.5);

    // Allow for more logical XY shifts in the presence of a custom rotation (for user input)
    uv_shift += vec2(0.5);
    uv_shift = matRot2D(uv_shift, 2*PI*(mat_rotate) / 360);
    uv_shift -= vec2(0.5);

    uv += uv_shift;

    float aa = 1.5 / (1000. * mat_aa);

    // deformation
    uv *= 2.;
    uv *= Rot(mat_time*.09);
    uv.x += atan(0.07, uv.y*uv.y*(sin(mat_time*.5)*.2+.2))*.4 * mat_deform_1;
    uv /= atan(4., mat_time*4.)+2. * mat_deform_2;
    uv *= Rot(mat_time*.2);
    uv.x *= uv.x;

    float d = length(uv+.14);
    vec3 col = vec3(.2, .0, .4);


    //circles
    float c1 = smoothstep(.2, .2+aa, d);
    float c2 = smoothstep(.1, .1+aa, d);
    float circle = mix(c1, c2, .87);
    col.rb += circle*.9;

    c1 = smoothstep(.3, .3+aa, d);
    c2 = smoothstep(.4, .4+aa, d);
    circle = mix(c1, c2, .5);
    col.r += circle*.1;

    c1 = smoothstep(.5, .5+aa, d);
    c2 = smoothstep(.6, .6+aa, d);
    circle = mix(c1, c2, .5);
    col.rg -= circle;

    c1 = smoothstep(.8, .8+aa, d);
    c2 = smoothstep(1., 1.+aa, d);
    circle = mix(c1, c2, .5);
    col.r += circle;

    c1 = smoothstep(1.2, 1.2+aa, d);
    c2 = smoothstep(1.4, 1.4+aa, d);
    circle = mix(c1, c2, .5);
    col.g -= circle;

    c1 = smoothstep(1.8, 1.8+aa, d);
    c2 = smoothstep(2.5, 2.5+aa, d);
    circle = mix(c1, c2, .5);
    col.r += circle;

    // color adjustment
    col.r = col.b - col.r *.8;
    col.b *= .13;
    col.g *= 1.2;
    col += .04;
    out_color = vec4(col,1.0);

    // Apply invert
    if (mat_invert) out_color.rgb=1-out_color.rgb;

    // Apply Hue Shift and saturation
    if (mat_hue_shift > 0.01 || mat_saturation != 0) {
        vec3 hsv = rgb2hsv(out_color.rgb);
        hsv.x = fract(0.9999999*(hsv.x+mat_hue_shift));
        hsv.y = max(hsv.y + mat_saturation, 0);
        out_color.rgb = hsv2rgb(hsv);
    }

    // Apply contrast
    const vec3 LumCoeff = vec3(0.2125, 0.7154, 0.0721);
    const vec3 AvgLumin = vec3(0.5, 0.5, 0.5);
    vec3 intensity = vec3(dot(out_color.rgb, LumCoeff));
    out_color.rgb = mix(AvgLumin, out_color.rgb, mat_contrast);

    // Apply brightness
    out_color.rgb += mat_brightness;

    return out_color;
}
