float df(vec2 p) {
  float m = 0.25 * mat_scale1;
  float l = length(p);
  l = mod(l+(0.5*m), m)-(0.5*m);
  return abs(l)-(m*0.25 * mat_scale2);
}

// License: MIT, author: Inigo Quilez, found: https://www.iquilezles.org/www/articles/smin/smin.htm
float pmin(float a, float b, float k) {
  float h = clamp(0.5+0.5*(b-a)/k, 0.0, 1.0);
  return mix(b, a, h) - k*h*(1.0-h);
}

// License: CC0, author: Mårten Rånge, found: https://github.com/mrange/glsl-snippets
float pmax(float a, float b, float k) {
  return -pmin(-a, -b, k);
}

vec3 effect(vec2 p, vec2 pp) {
  float aa = 2.0/(1000. * mat_detail);
  float tm = mat_time*0.3;
  vec2 p0 = p+sin(vec2(1.0, sqrt(0.5))*(tm+100.0));
  vec2 p1 = p+sin(1.2*vec2(1.0, sqrt(0.5))*(tm+200.0));
  float sm = 0.0666*length(p) * mat_thresh;
  float d0 = df(p0);
  float d1 = df(p1);
  float d = d0;
  d = pmax(d, d1, sm);
  float dd = -d0;
  dd = pmax(dd, -d1, sm);
  d =  min(d, dd);
  float so = 8.0 / mat_glow;
  const float co = 0.5;
  vec3 bcol0 = (1.0+sin(vec3(0.0, 1.0, 2.0) + co*length(p0)+1.0-mat_time))/(so*dot(p0, p0)+0.0001);
  vec3 bcol1 = (1.0+sin(vec3(0.0, 1.0, 2.0) + co*length(p1)+3.0+mat_time))/(so*dot(p1, p1)+0.0001);
  vec3 bcol = (bcol0+bcol1);
  vec3 col = vec3(0.0);
  col += 0.005*bcol/(max(dd+0.005, 0.0)+0.0001);
  col = mix(col, bcol, smoothstep(aa, -aa, d));
  col -= mat_spot*0.25*vec3(0.0, 1.0, 2.0).zyx*length(pp);
  col *= smoothstep(1.5, 0.5, length(pp));
  col = clamp(col, 0.0, 1.0);
  col = sqrt(col);
  return col;
}


vec4 materialColorForPixel(vec2 texCoord)
{
    vec4 out_color; // Final output color
    vec2 uv = texCoord - vec2(0.5);

    uv = transformUV(uv);

    vec2 q = uv + vec2(0.5);
    vec2 p = uv;
    vec2 pp = p;
    vec3 col = effect(p, pp);

    out_color = vec4(col, 1.0);

    // Luma to alpha (before color controls)
    if (mat_luma_to_alpha && mat_luma_mode == 0) {
        out_color.a = mat_luma(out_color * mat_luma_sensitivity * 2.) - mat_luma_threshold * 4.;
    }

    // Apply invert
    if (mat_invert) out_color.rgb=1-out_color.rgb;

    // Apply Hue Shift and saturation
    if (mat_hue_shift > 0.01 || mat_saturation != 0) {
        vec3 hsv = rgb2hsv(out_color.rgb);
        hsv.x = fract(0.9999999*(hsv.x+mat_hue_shift));
        hsv.y = max(hsv.y + mat_saturation, 0);
        out_color.rgb = hsv2rgb(hsv);
    }

    // Apply contrast
    const vec3 LumCoeff = vec3(0.2125, 0.7154, 0.0721);
    const vec3 AvgLumin = vec3(0.5, 0.5, 0.5);
    vec3 intensity = vec3(dot(out_color.rgb, LumCoeff));
    out_color.rgb = mix(AvgLumin, out_color.rgb, mat_contrast);

    // Apply brightness
    out_color.rgb += mat_brightness;

    // Luma to alpha (after color controls)
    if (mat_luma_to_alpha && mat_luma_mode == 1) {
        out_color.a = mat_luma(out_color * mat_luma_sensitivity * 2.) - mat_luma_threshold * 4.;
    }

    return out_color;
}
