vec4 materialColorForPixel(vec2 texCoord)
{
    vec4 out_color; // Final output color
    vec2 uv = texCoord - vec2(0.5);

    vec2 uv_orig = uv;

    uv = transformUV(uv);

    float s = 1.2;

    vec2 u = uv;

    float count = int(mat_count * 2);

    float square = 1.;

    if (mat_border) {
        square = step(abs(u.x),.5)*step(abs(u.y),.5);
    }

    vec2 cell_offset = mat_cell_offset;
    cell_offset += vec2(0.5);
    cell_offset.x = 1.-cell_offset.x;
    cell_offset -= vec2(0.5);
    cell_offset *= 8.;

    vec2 cell = floor((u+.5)*count);

    cell += cell_offset;

    cell *= mat_cell_scale;


    float index = cell.x+cell.y*count;
    float tixel = float(tixy(mat_time,index,cell.x,cell.y));

    float dist = length(fract(u*count)-.5)*count/.5;

    float radius = count*min(abs(tixel),1.0);

    dist *= mat_sharp * 1000.;
    radius *= mat_size * mat_sharp * 1000.;

    vec3 col = tixel<0. ? vec3(255,34,68)/255. : vec3(1);

    col *= clamp((radius-dist)/s/count/count/2.,0.,1.)*square;

    out_color = vec4(col,1.);

    // Apply invert
    if (mat_invert) out_color.rgb=1-out_color.rgb;

    // Apply Hue Shift and saturation
    if (mat_hue_shift > 0.01 || mat_saturation != 0) {
        vec3 hsv = rgb2hsv(out_color.rgb);
        hsv.x = fract(0.9999999*(hsv.x+mat_hue_shift));
        hsv.y = max(hsv.y + mat_saturation, 0.);
        out_color.rgb = hsv2rgb(hsv);
    }

    // Apply contrast
    const vec3 LumCoeff = vec3(0.2125, 0.7154, 0.0721);
    const vec3 AvgLumin = vec3(0.5, 0.5, 0.5);
    vec3 intensity = vec3(dot(out_color.rgb, LumCoeff));
    out_color.rgb = mix(AvgLumin, out_color.rgb, mat_contrast);

    // Apply brightness
    out_color.rgb += mat_brightness;


    return out_color;
}
