#include "MadCommon.glsl"

float mat_time = (mat_time_source - mat_offset * 8. * mat_offset_scale) * 0.25;

vec2 matRot2D(vec2 _st, float _angle){
    _st -= 0.5;
    _st =  mat2(cos(_angle),-sin(_angle),
                sin(_angle),cos(_angle)) * _st;
    _st += 0.5;
    return _st;
}

vec2 mirrorUV(vec2 uv) {
    uv += vec2(0.5);
    if (mat_mirror_x) {
        if (uv.x > 0.5)   {
            uv.x = 1.0-uv.x;
        }
    }
    if (mat_mirror_y) {
        if (uv.y > 0.5) {
            uv.y = 1.0-uv.y;
        }
    }
    uv -= vec2(0.5);
    return uv;
}

vec2 transformUV(vec2 uv) {

    if (mat_lock_aspect) {
        uv.x *= RENDERSIZE.x / RENDERSIZE.y;
    }
    uv *= mat_scale;

    uv = mirrorUV(uv);

    vec2 uv_shift = mat_shift_amount * mat_shift_scale;
    uv_shift += vec2(0.5);
    uv_shift.x = 1.-uv_shift.x;
    uv_shift -= vec2(0.5);

    // XY shift pre rotate
    if (mat_shift_type == 0) {

        // Preserve direction
        uv_shift += vec2(0.5);
        uv_shift = matRot2D(uv_shift, -1. * 2*PI*(mat_rotate) / 360);
        uv_shift -= vec2(0.5);
        uv += uv_shift;
    }

    uv += vec2(0.5);
    uv = matRot2D(uv, 2*PI*mat_rotate / 360);
    uv -= vec2(0.5);

    // XY shift post rotate
    if (mat_shift_type == 1) {

        // Preserve direction
        uv_shift += vec2(0.5);
        uv_shift = matRot2D(uv_shift, 2*PI*(mat_rotate) / 360);
        uv_shift -= vec2(0.5);
        uv += uv_shift;
    }

    return uv;
}


// cylinder cross
float sc2(vec3 p, float r) {
    float s1 = length(p.xz);
    float s2 = length(p.xy);
    float s3 = length(p.zy);
    return min(s1, min(s2, s3)) - r;
}

mat2 r2d(float a) {
    float c = cos(a), s = sin(a);
    return mat2(c, s, -s, c);
}

float de(vec3 p) {
    float d = 0., s = 1.;

    p.xy *= r2d(cos(mat_time));

    p.y -= .2;
    p.x -= .4;

    p.x = abs(p.x) - .6;

    if (mod(p.z, 2.) > 1.)
        p.xy *= r2d(cos(mat_time + p.x)*.5);
    else
        p.xy *= r2d(-mat_time + p.z);

    vec3 q = p;

    for (int i = 0; i < 5; i++) {
        q = mod(p*s + 1., 4.) - 1.;
        d = max(d, -sc2(q, .8) / s);
        s += 6.;
    }

    return d * mat_distort;
}

vec4 materialColorForPixel(vec2 texCoord)
{
    vec4 out_color; // Final output color
    vec2 uv = texCoord - vec2(0.5);

    uv = transformUV(uv);

    float dt = mat_time * 2.;
    float a = cos(dt)*.3;
    float b = sin(dt)*.2;
    vec3 ro = vec3(a, b, -mat_time*8.), p;
    vec3 rd = normalize(vec3(uv, -1));
    p = ro;
    float i = 0.;
    float d;
    for (float it=0.; it < 1.; it += .04 * mat_step) {
        i = it;
        d = de(p);
        if (d < .001) break;
        p += rd*d;
    }
    i /= .1 * tan(3.*p.x + p.y*p.y) * mat_frost;
    vec3 c = mix(vec3(.4, .5, .6) * mat_color_fact_1, vec3(.0, .2, .3) * mat_color_fact_2, i*tan(p.z + p.y + cos(p.x + mat_time*1.)));
    out_color = vec4(c, 1.0);


    // Apply invert
    if (mat_invert) out_color.rgb=1-out_color.rgb;

    // Apply Hue Shift and saturation
    if (mat_hue_shift > 0.01 || mat_saturation != 0) {
        vec3 hsv = rgb2hsv(out_color.rgb);
        hsv.x = fract(0.9999999*(hsv.x+mat_hue_shift));
        hsv.y = max(hsv.y + mat_saturation, 0);
        out_color.rgb = hsv2rgb(hsv);
    }

    // Apply contrast
    const vec3 LumCoeff = vec3(0.2125, 0.7154, 0.0721);
    const vec3 AvgLumin = vec3(0.5, 0.5, 0.5);
    vec3 intensity = vec3(dot(out_color.rgb, LumCoeff));
    out_color.rgb = mix(AvgLumin, out_color.rgb, mat_contrast);

    // Apply brightness
    out_color.rgb += mat_brightness;


    return out_color;
}
